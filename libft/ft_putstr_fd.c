#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int	n;

	if (s != NULL)
	{
		n = 0;
		while (s[n] != '\0')
		{
			ft_putchar_fd(s[n], fd);
			n++;
		}
	}
}
