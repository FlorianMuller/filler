#include "libft.h"

void	ft_set_bit(void *ptr, int value, size_t n)
{
	char	*p;
	char	one;

	p = ptr;
	one = 1;
	while (n > 8)
	{
		p++;
		n -= 8;
	}
	n--;
	if (value)
		*p = ((1U << n) | *p);
	else
		*p = (~(1U << n) & *p);
}
