#include "libft.h"

int		ft_read_bit(void *ptr, size_t n)
{
	char	*p;

	p = ptr;
	while (n > 8)
	{
		p++;
		n -= 8;
	}
	n--;
	if ((1U << n) & *p)
		return (1);
	else
		return (0);
}
